/**
 * 格式化 "省 · 市" 字符串
 */
export function formatLocation(province, city) {
  if (!province && !city) return '';
  if (!province) return city;
  if (!city) return province;
  return `${province} · ${city}`;
}

/**
 * 解析 "省 · 市" 为 { province, city }
 */
export function parseLocation(locationStr) {
  if (!locationStr) return { province: '', city: '' };
  const parts = locationStr.split(' · ');
  return {
    province: parts[0] || '',
    city: parts[1] || parts[0] || '',
  };
}
